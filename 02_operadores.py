# Operadores

print("1 + 1", 1 + 1)
print("2 - 1", 2 - 1)
print("2 * 2", 2 * 2)
print("2 / 2", 2 / 2)
print("2 ** 2", 2 ** 2)
print("5 % 2", 5 % 2)

print("concatenacion de string", "hola " + "Python")
print("multiplicacion de string", "hola " * 3)

print("2 > 1", 2 > 1)
print("2 < 1", 2 < 1)
print("2 >= 1", 2 >= 1)
print("2 <= 1", 2 <= 1)
print("2 == 1", 2 == 1)
print("2 != 1", 2 != 1)

print("Hola > Python", "Hola" > "Python") #ordenación alfabética por ASCII
print("len(hola) > len(python)", len("hola") > len("python")) #longitud

print("hola in hola mundo", "hola" in "hola mundo")
print("hola not in hola mundo", "hola" not in "hola mundo")
print("hola in [hola, mundo]", "hola" in ["hola", "mundo"])

print("2 > 1 and 3 > 2", 2 > 1 and 3 > 2)
print("2 > 1 or 3 > 2", 2 > 1 or 3 > 2)
print("not 2 > 1", not 2 > 1)