# Variables

my_string_variable = "My String Variable"
print('string', my_string_variable)

my_int_variable = 10
print('int', my_int_variable)

my_float_variable = 10.2
print('float', my_float_variable)

my_bool_variable = True
print('bool', my_bool_variable)

my_int_to_string = str(my_int_variable)
print('int to string', my_int_to_string)
print('type of int to string', type(my_int_to_string))


